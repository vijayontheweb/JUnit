package com.gavs.calculator;

public class Calculator {	
	
	int add(int arg1, int arg2) {
		return arg1+arg2;
	}
	
	int subtract(int arg1, int arg2) {
		return arg1-arg2;		
	}
	
	int multiply(int arg1, int arg2) {
		return arg1*arg2;		
	}
	
	int divide(int arg1, int arg2) {
		return arg1/arg2;		
	}
}
